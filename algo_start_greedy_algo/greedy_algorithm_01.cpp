// РѕС‚ РѕР±С‰РµРіРѕ С‡РёСЃР»Р° РєСѓР±РёРєРѕРІ РѕРґРёРЅ (РІРµРґСЊ РјС‹ РµРіРѕ СѓР¶Рµ Р·Р°РїРёСЃР°Р»Рё РµРіРѕ Р·РЅР°С‡РµРЅРёРµ)
// С‚Р°РєРёРј РѕР±СЂР°Р·РѕРј РЅР° РєР°Р¶РґРѕРј С€Р°РіРµ РІСЃРµРіРґР° РёС‰РµС‚СЃСЏ СЂР°РІРЅРѕРјРµСЂРЅРѕРµ С‡РёСЃР»Рѕ РјРµР¶РґСѓ РѕСЃС‚Р°РІС€РёРјРёСЃСЏ РєСѓР±РёРєР°РјРё 

int main() {
    int t;
    std::cin >> t;

    std::vector<std::vector<int>> n_s_r(t, std::vector<int>(3));

    for (int i = 0; i < t; ++i) {
        for (int j = 0; j < 3; ++j) {
            std::cin >> n_s_r[i][j];
        }
    }

    std::vector<std::vector<int>> ans(t);

    for (int i = 0; i < t; ++i) {
        int n = n_s_r[i][0];
        int s = n_s_r[i][1];
        int r = n_s_r[i][2];

        int removed = s - r;
        ans[i].push_back(removed);

        int remaining = n - 1;
        int current_r = r;

        for (int j = 0; j < remaining; ++j) {
            int available = remaining - j;
            int elem = current_r / available;
            ans[i].push_back(elem);
            current_r -= elem;
        }
    }

    for (const auto& vec : ans) {
        for (int val : vec) {
            std::cout << val << ' ';
        }
        std::cout << std::endl;
    }

    return 0;
}